public class Medico {
    private String nome;
    private String email;
    private Integer crm;

    public String getNome() {
        return nome;
    }

    public String getEmail() {
        return email;
    }

    public Integer getCrm() {
        return crm;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public void setEmail(String email) {
        this.email = email;
    }

}
